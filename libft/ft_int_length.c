int		ft_int_length(int n)
{
	int				count;
	unsigned int	nb;

	nb = n;
	count = 1;
	if (n < 0)
	{
		nb = -n;
		count++;
	}
	while (nb > 9)
	{
		nb /= 10;
		count++;
	}
	return (count);
}
